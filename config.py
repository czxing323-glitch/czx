"""
config.py: 全局系统参数与智能体超参数配置文件
"""

class Config:
    # SCMA 资源配置 (4行6列，每个资源块承载3个用户)
    K = 4  # 资源块(RE)数量
    J = 6  # 用户数量
    V = 3  # 每个RE上的用户叠加数
    
    # 基础星座大小 (假设每个用户使用 4-BPSK 或 4-QAM 维度的基础星座，共有4个点)
    # 为了简化计算，我们设基础QAM点集为 [-1, 1] 的复数空间组合
    BASE_M = 4 
    
    # 多智能体优化配置
    DELTA_THETA = 0.05  # 智能体微调步长 (弧度 rad, 约 2.8 度)
    MAX_EPOCHS = 30     # 多智能体协同大循环最大轮数
    SIGMA_0 = 0.2       # Soft-Sum 势能打分器的连续平滑因子
    
    # 链路测试配置
    SNR_RANGE = range(0, 13, 2)  # 仿真信噪比范围 (dB)
    NUM_BITS = 24000            # 蒙特卡洛仿真发送的总比特数
